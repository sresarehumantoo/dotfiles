func TestScanCustomShellFiles_FindsCustomRC(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	// Create custom shell files
	os.WriteFile(filepath.Join(home, ".companyrc"), []byte("# work stuff"), 0644)
	os.WriteFile(filepath.Join(home, ".work_env"), []byte("export FOO=1"), 0644)
	os.WriteFile(filepath.Join(home, ".localrc"), []byte("# local"), 0644)

	discovered := modules.ScanCustomShellFiles()

	found := make(map[string]bool)
	for _, d := range discovered {
		found[d.RelPath] = true
	}

	for _, want := range []string{".companyrc", ".work_env", ".localrc"} {
		if !found[want] {
			t.Errorf("expected to find %q in scan results, got %v", want, discovered)
		}
	}
}

func TestScanCustomShellFiles_IgnoresManaged(t *testing.T) {
	home := t.TempDir()
	t.Setenv("HOME", home)

	// Create files that dfinstall manages — these should be excluded
	os.WriteFile(filepath.Join(home, ".zshrc"), []byte("# managed"), 0644)
	os.WriteFile(filepath.Join(home, ".bashrc"), []byte("# managed"), 0644)
	os.WriteFile(filepath.Join(home, ".aliases"), []byte("# managed"), 0644)

	discovered := modules.ScanCustomShellFiles()

	for _, d := range discovered {
		if d.RelPath == ".zshrc" || d.RelPath == ".bashrc" || d.RelPath == ".aliases" {
			t.Errorf("scan should not include managed file %q", d.RelPath)
		}
	}
}

